#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	i;

	i = 0;
	if (SIZE_MAX - 8192 > ft_strlen(s1))
		str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	else
		return (NULL);
	if (str == NULL)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
